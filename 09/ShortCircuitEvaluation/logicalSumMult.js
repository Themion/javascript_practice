// 논리합 =>  a || b
// if (a) return a
// else if (b) return b
// else return false
console.log(`'Cat' || 'Dog': ${'Cat' || 'Dog'}`)
console.log(`false || 'Dog': ${false || 'Dog'}`)
console.log(`'Cat' || false: ${'Cat' || false}\n`)

// 논리곱 => a && b
// return return (!!a && !!b) ? b : false
console.log(`'Cat' && 'Dog': ${'Cat' && 'Dog'}`)
console.log(`false && 'Dog': ${false && 'Dog'}`)
console.log(`'Cat' && false: ${'Cat' && false}\n`)
